// var, let y const
/* En este archivo
vamos aprender los variables */

var edad = 30;
var edad = 40;
/* Problema: estamos declarando 2 veces la misma variable y le estamos cambiando el valor */
console.log(edad);

let altura = 150; // declarando por que usamos let
altura = 200; // aca estamos cambiando su valor y no declarando
console.log(altura);

// const "Constante"
const velocidad = 100;
velocidad = 400;
console.log(velocidad);